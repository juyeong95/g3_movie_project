package com.care.root.book.dto;

public class BookInfoDTO {
	private int num;
	private String title;
	private int days;
	private String times;
	private int theater;
	private String seats;
	private String pn;
	private int getNum() {
		return num;
	}
	private void setNum(int num) {
		this.num = num;
	}
	private String getTitle() {
		return title;
	}
	private void setTitle(String title) {
		this.title = title;
	}
	private int getDays() {
		return days;
	}
	private void setDays(int days) {
		this.days = days;
	}
	private String getTimes() {
		return times;
	}
	private void setTimes(String times) {
		this.times = times;
	}
	private int getTheater() {
		return theater;
	}
	private void setTheater(int theater) {
		this.theater = theater;
	}
	private String getSeats() {
		return seats;
	}
	private void setSeats(String seats) {
		this.seats = seats;
	}
	private String getPn() {
		return pn;
	}
	private void setPn(String pn) {
		this.pn = pn;
	}
	
	
	
	
	
}
